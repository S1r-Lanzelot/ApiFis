const getCountryFlag = (fifaCountryCode) => {
  return countryISOMapping[fifaCountryCode];
};

export const getFlagEmoji = (fifaCountryCode) => {
  const isoCountryCode = getCountryFlag(fifaCountryCode);
  if (!isoCountryCode) {
    return String.fromCodePoint(0x1f3f4, 0xe0067, 0xe0062, 0xe0074, 0xe007f);
  }

  const codePoints = isoCountryCode
    .toUpperCase()
    .split("")
    .map((char) => 127397 + char.charCodeAt());

  return String.fromCodePoint(...codePoints);
};
